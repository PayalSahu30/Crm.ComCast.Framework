package preProgramming;

public class For_loop 
{
public static void main(String[] args) {
	for (int i = 1238; i >= 1128; i--) {
		System.out.print(i+ " ");
	}
	System.out.println();
	
	for (int i = 123; i >= 113; i--) {
		System.out.print(i+ " ");
	}
	System.out.println();
	
	for (char i = 'k'; i <= 'x'; i++) {
		System.out.print(i+ " ");
	}
	System.out.println();
	
	for (char i = 'z'; i >= 'o'; i--) {
		System.out.print(i+ " ");
	}
	System.out.println();
	
	for (int i = 0; i >= -123; i--) {
		System.out.print(i+ " ");
	}
	System.out.println();
	
	for (int i = -13; i <= 1; i++) {
		System.out.print(i+ " ");
	}
	System.out.println();
	
	for (int i = -14; i <= 14; i++) {
		System.out.print(i+ " ");
	}
	System.out.println();
}
}
